package com.dabangvr.dep.model;

public class DepOrderGoods {
    private String id;
    private String deliveryGoodsTypeId;
    private String name;
    private String listUrl;
    private String sellingPrice;
    private String marketPrice;
    private String startingPrice;
    private String number;
    private String productId;
    private String productValue;

    public DepOrderGoods() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDeliveryGoodsTypeId() {
        return deliveryGoodsTypeId;
    }

    public void setDeliveryGoodsTypeId(String deliveryGoodsTypeId) {
        this.deliveryGoodsTypeId = deliveryGoodsTypeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getListUrl() {
        return listUrl;
    }

    public void setListUrl(String listUrl) {
        this.listUrl = listUrl;
    }

    public String getSellingPrice() {
        return sellingPrice;
    }

    public void setSellingPrice(String sellingPrice) {
        this.sellingPrice = sellingPrice;
    }

    public String getMarketPrice() {
        return marketPrice;
    }

    public void setMarketPrice(String marketPrice) {
        this.marketPrice = marketPrice;
    }

    public String getStartingPrice() {
        return startingPrice;
    }

    public void setStartingPrice(String startingPrice) {
        this.startingPrice = startingPrice;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getProductValue() {
        return productValue;
    }

    public void setProductValue(String productValue) {
        this.productValue = productValue;
    }
}
